// Filtrar los datos segn los criterios seleccionados
document.getElementById('apply-filters').addEventListener('click', function() {
    const estacionOptions = document.getElementById('estacion').selectedOptions;
    const contaminante = parseInt(document.getElementById('contaminante').value);
    const fechaInicio = document.getElementById('fecha-inicio').value;
    const fechaFin = document.getElementById('fecha-fin').value;

    // Obtener un array con los IDs de las estaciones seleccionadas
    const estacionesSeleccionadas = Array.from(estacionOptions).map(option => parseInt(option.value));

    // Filtrar los datos segn las estaciones seleccionadas, contaminante y fechas
    const filteredData = data.filter(item => {
        return estacionesSeleccionadas.includes(item.estacion_id) && 
               (item.contaminante_id === contaminante) &&
               (!fechaInicio || new Date(item.fecha) >= new Date(fechaInicio)) &&
               (!fechaFin || new Date(item.fecha) <= new Date(fechaFin));
    });

    // Mostrar los resultados en la tabla
    const tableBody = document.querySelector('#results-table tbody');
    tableBody.innerHTML = ''; // Limpiar la tabla antes de llenarla
    if (filteredData.length > 0) {
        filteredData.forEach(item => {
            const row = document.createElement('tr');
            row.innerHTML = `<td>${item.fecha}</td><td>${item.valor}</td>`;
            tableBody.appendChild(row);
        });
        // Calcular las estadsticas
        calculateStats(filteredData);
        // Actualizar grfico
        updateChart(filteredData);
    } else {
        tableBody.innerHTML = '<tr><td colspan="2">No hay datos disponibles para los filtros seleccionados.</td></tr>';
        // Mostrar un mensaje en el grfico que indica que no hay datos
        document.getElementById('chart-panel').innerHTML = 'No hay datos disponibles para los filtros seleccionados.';
    }
});

// Calcular estadsticas
function calculateStats(filteredData) {
    const values = filteredData.map(item => item.valor);
    
    const average = math.mean(values);
    const stdDev = math.std(values);
    const min = math.min(values);
    const max = math.max(values);
    const median = math.median(values);
    const range = max - min;
    const q1 = math.quantileSeq(values, 0.25);
    const q3 = math.quantileSeq(values, 0.75);

    // Mostrar estadsticas en la UI
    document.getElementById('average').textContent = average.toFixed(2);
    document.getElementById('std-dev').textContent = stdDev.toFixed(2);
    document.getElementById('max').textContent = max.toFixed(2);
    document.getElementById('min').textContent = min.toFixed(2);
    document.getElementById('median').textContent = median.toFixed(2);
    document.getElementById('range').textContent = range.toFixed(2);
    document.getElementById('q1').textContent = q1.toFixed(2);
    document.getElementById('q3').textContent = q3.toFixed(2);
}

// Actualizar el grfico
function updateChart(filteredData) {
    const values = filteredData.map(item => item.valor);
    const dates = filteredData.map(item => new Date(item.fecha).toLocaleDateString());

    // Verificar que hay datos para graficar
    if (values.length === 0) {
        console.error("No hay datos para graficar");
        // Mostrar un mensaje en el grfico que indica que no hay datos
        document.getElementById('chart-panel').innerHTML = 'No hay datos disponibles para los filtros seleccionados.';
        return;
    }

    const ctx = document.getElementById('chart').getContext('2d');

    // Verificar si el contexto es vlido
    if (!ctx) {
        console.error("No se pudo obtener el contexto del canvas");
        return;
    }

    // Si el grfico ya existe, destryelo antes de crear uno nuevo
    if (window.chart instanceof Chart) {
        window.chart.destroy();
    }

    // Crear el grfico
    window.chart = new Chart(ctx, {
        type: 'line',
        data: {
            labels: dates,
            datasets: [{
                label: 'Valores de Contaminante',
                data: values,
                borderColor: 'rgba(75, 192, 192, 1)',
                fill: false
            }]
        },
        options: {
            responsive: true,
            scales: {
                x: {
                    title: {
                        display: true,
                        text: 'Fecha'
                    }
                },
                y: {
                    title: {
                        display: true,
                        text: 'Valor'
                    }
                }
            }
        }
    });

    console.log("Grfico actualizado con xito");
}
